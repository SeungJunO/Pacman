    import random
    options = ['왼쪽','오른쪽']
    player = input("왼쪽과 오른쪽을 선택하세요:")
    computer = random.choice(options)
    
    if (computer == player):
        if (player == "왼쪽"):
            t.goto(-50, y + step*100)
            t.rt(360)
        elif(player == "오른쪽"):
            t.goto(50, y + step*100)
            t.rt(360)
    
    elif(computer != player):
        print("잘못된 선택으로 당신의 거북이는 죽습니다.")
        break
    step = step + 1


